"""Generate LaTeX macros and tables from strict-70 source data."""

from __future__ import annotations

import json
from pathlib import Path

import pandas as pd


ROOT = Path(__file__).resolve().parents[2]
RESULTS = ROOT / "experiment" / "results" / "same_budget"
REPORT = ROOT / "experiment" / "report"


def command(name: str, value: str) -> str:
    return rf"\newcommand{{\{name}}}{{{value}}}"


def main() -> None:
    summary = pd.read_csv(RESULTS / "strict70_summary.csv", encoding="utf-8-sig").set_index("preset")
    members = pd.read_csv(RESULTS / "strict70_member_metrics.csv", encoding="utf-8-sig")
    bootstrap = pd.read_csv(RESULTS / "strict70_bootstrap.csv", encoding="utf-8-sig")
    analysis = json.loads((RESULTS / "strict70_analysis.json").read_text(encoding="utf-8"))
    dapt = json.loads((RESULTS / "dapt_result.json").read_text(encoding="utf-8"))

    base = summary.loc["base_ce"]
    full = summary.loc["dapt_rdrop"]
    paired = analysis["paired_initialization_statistics"]["dapt_rdrop"]
    row_bootstrap = bootstrap[
        (bootstrap["comparison"] == "dapt_rdrop-base_ce") & (bootstrap["unit"] == "row")
    ].iloc[0]
    cluster_bootstrap = bootstrap[
        (bootstrap["comparison"] == "dapt_rdrop-base_ce")
        & (bootstrap["unit"] == "live_id_cluster")
    ].iloc[0]
    overlap = analysis["exact_content_overlap_audit"]

    macros = [
        "% Generated by make_same_budget_report_assets.py.",
        command("StrictPaperFOne", f"{100 * analysis['paper_reference_f1']:.2f}"),
        command("StrictBaseMemberMean", f"{100 * base.member_mean_macro_f1:.2f}"),
        command("StrictBaseMemberSD", f"{100 * base.member_sd_macro_f1:.2f}"),
        command("StrictBaseEnsemble", f"{100 * base.ensemble_macro_f1:.2f}"),
        command("StrictFullMemberMean", f"{100 * full.member_mean_macro_f1:.2f}"),
        command("StrictFullMemberSD", f"{100 * full.member_sd_macro_f1:.2f}"),
        command("StrictFullEnsemble", f"{100 * full.ensemble_macro_f1:.2f}"),
        command("StrictFullMemberPaperDelta", f"{full.delta_member_mean_vs_paper_pp:+.2f}"),
        command("StrictFullEnsemblePaperDelta", f"{full.delta_ensemble_vs_paper_pp:+.2f}"),
        command("StrictFullEnsembleBaseDelta", f"{full.delta_ensemble_vs_base_pp:+.2f}"),
        command("StrictPairedMeanDelta", f"{paired['mean_delta_pp']:+.2f}"),
        command("StrictPairedCILow", f"{paired['t_ci95_pp'][0]:+.2f}"),
        command("StrictPairedCIHigh", f"{paired['t_ci95_pp'][1]:+.2f}"),
        command("StrictPairedTPValue", f"{paired['paired_t_pvalue']:.4f}"),
        command("StrictSignPValue", f"{paired['exact_two_sided_sign_test_pvalue']:.4f}"),
        command("StrictRowBootstrapLow", f"{row_bootstrap.ci95_low_pp:+.2f}"),
        command("StrictRowBootstrapHigh", f"{row_bootstrap.ci95_high_pp:+.2f}"),
        command("StrictClusterBootstrapLow", f"{cluster_bootstrap.ci95_low_pp:+.2f}"),
        command("StrictClusterBootstrapHigh", f"{cluster_bootstrap.ci95_high_pp:+.2f}"),
        command("StrictFullAccuracy", f"{100 * full.ensemble_accuracy:.2f}"),
        command("StrictFullMacroPrecision", f"{100 * full.ensemble_macro_precision:.2f}"),
        command("StrictFullMacroRecall", f"{100 * full.ensemble_macro_recall:.2f}"),
        command("StrictBaseAccuracy", f"{100 * base.ensemble_accuracy:.2f}"),
        command("StrictBaseMacroPrecision", f"{100 * base.ensemble_macro_precision:.2f}"),
        command("StrictBaseMacroRecall", f"{100 * base.ensemble_macro_recall:.2f}"),
        command("StrictDaptRows", f"{int(dapt['input_rows']):,}"),
        command("StrictDaptSeconds", f"{dapt['elapsed_seconds']:.1f}"),
        command("StrictDaptPeakGiB", f"{dapt['peak_torch_memory_mb'] / 1024:.2f}"),
        command("StrictTestExactOverlapRows", f"{int(overlap['test_rows_with_exact_train_content']):,}"),
        command("StrictTestExactOverlapPercent", f"{100 * overlap['test_fraction_with_exact_train_content']:.2f}"),
    ]
    (REPORT / "same_budget_macros.tex").write_text("\n".join(macros) + "\n", encoding="utf-8")

    labels = {
        "base_ce": "基础 RoBERTa + CE",
        "dapt_ce": "DAPT RoBERTa + CE",
        "base_rdrop": "基础 RoBERTa + R-Drop",
        "dapt_rdrop": "DAPT RoBERTa + R-Drop",
    }
    table = [
        r"\begin{tabular}{lccrrrr}",
        r"\toprule",
        r"方法 & DAPT & R-Drop & 单成员均值$\pm$SD & 同标签集成 & 对论文 & 对基线\\",
        r"\midrule",
    ]
    for preset in ("base_ce", "dapt_ce", "base_rdrop", "dapt_rdrop"):
        row = summary.loc[preset]
        prefix = r"\rowcolor{LightGreen}" if preset == "dapt_rdrop" else ""
        table.append(
            prefix
            + f"{labels[preset]} & {'是' if row.dapt else '否'} & {'是' if row.rdrop else '否'} & "
            + f"{100 * row.member_mean_macro_f1:.2f}$\\pm${100 * row.member_sd_macro_f1:.2f} & "
            + f"{100 * row.ensemble_macro_f1:.2f} & {row.delta_ensemble_vs_paper_pp:+.2f} & "
            + f"{row.delta_ensemble_vs_base_pp:+.2f}\\\\"
        )
    table.extend([r"\bottomrule", r"\end{tabular}"])
    (REPORT / "table_same_budget.tex").write_text("\n".join(table) + "\n", encoding="utf-8")

    base_members = members[members["preset"] == "base_ce"].set_index("seed")
    full_members = members[members["preset"] == "dapt_rdrop"].set_index("seed")
    member_table = [
        r"\begin{tabular}{rrrr}",
        r"\toprule",
        r"初始化种子 & 基线 Macro-F1 & DAPT+R-Drop Macro-F1 & 配对差值（pp）\\",
        r"\midrule",
    ]
    for seed in sorted(base_members.index):
        base_value = float(base_members.loc[seed, "macro_f1"])
        full_value = float(full_members.loc[seed, "macro_f1"])
        member_table.append(f"{int(seed)} & {100 * base_value:.2f} & {100 * full_value:.2f} & {100 * (full_value - base_value):+.2f}\\\\")
    member_table.extend(
        [
            r"\midrule",
            f"均值$\\pm$SD & {100 * base.member_mean_macro_f1:.2f}$\\pm${100 * base.member_sd_macro_f1:.2f} & "
            f"{100 * full.member_mean_macro_f1:.2f}$\\pm${100 * full.member_sd_macro_f1:.2f} & {paired['mean_delta_pp']:+.2f}\\\\",
            r"\bottomrule",
            r"\end{tabular}",
        ]
    )
    (REPORT / "table_same_budget_members.tex").write_text("\n".join(member_table) + "\n", encoding="utf-8")

    bootstrap_table = [
        r"\begin{tabular}{lrrr}",
        r"\toprule",
        r"重采样单位 & 集成差值（pp） & 95\% CI（pp） & $P(\Delta\leq0)$\\",
        r"\midrule",
        f"逐行 & {row_bootstrap.point_delta_pp:+.2f} & [{row_bootstrap.ci95_low_pp:+.2f}, {row_bootstrap.ci95_high_pp:+.2f}] & {row_bootstrap.probability_delta_le_zero:.4f}\\\\",
        f"按直播间聚类（12 组） & {cluster_bootstrap.point_delta_pp:+.2f} & [{cluster_bootstrap.ci95_low_pp:+.2f}, {cluster_bootstrap.ci95_high_pp:+.2f}] & {cluster_bootstrap.probability_delta_le_zero:.4f}\\\\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    (REPORT / "table_same_budget_bootstrap.tex").write_text("\n".join(bootstrap_table) + "\n", encoding="utf-8")
    print("Wrote same_budget_macros.tex and three strict-70 tables")


if __name__ == "__main__":
    main()
