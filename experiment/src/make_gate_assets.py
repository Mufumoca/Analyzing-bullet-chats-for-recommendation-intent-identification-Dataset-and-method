"""Generate the report table and figure for the uncertainty-gate experiment."""

from __future__ import annotations

import json
from pathlib import Path

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

plt.rcParams["font.family"] = "sans-serif"
plt.rcParams["font.sans-serif"] = ["Arial", "DejaVu Sans", "Liberation Sans"]
plt.rcParams["svg.fonttype"] = "none"
plt.rcParams["pdf.fonttype"] = 42
plt.rcParams.update(
    {
        "font.size": 8,
        "axes.spines.right": False,
        "axes.spines.top": False,
        "axes.linewidth": 0.8,
        "legend.frameon": False,
    }
)

ROOT = Path(__file__).resolve().parents[2]
RESULTS = ROOT / "experiment" / "results" / "improvement"
FIGURES = ROOT / "experiment" / "figures"
REPORT = ROOT / "experiment" / "report"


def main() -> None:
    result = json.loads((RESULTS / "uncertainty_gate.json").read_text(encoding="utf-8"))
    test = result["test"]
    validation = result["validation"]
    selection = result["selection"]

    base = float(test["base"]["macro_f1"])
    qwen = float(test["qwen"]["macro_f1"])
    gate = float(test["gate"]["macro_f1"])
    delta = float(test["gate_vs_base"]["macro_f1_difference"])
    ci_low = float(test["gate_vs_base"]["macro_f1_ci95_low"])
    ci_high = float(test["gate_vs_base"]["macro_f1_ci95_high"])

    source = pd.DataFrame(
        [
            {"split": "validation", "method": "TF-IDF base", "macro_f1": validation["base"]["macro_f1"], "replacement_fraction": 0.0},
            {"split": "validation", "method": "Qwen direct", "macro_f1": validation["qwen"]["macro_f1"], "replacement_fraction": 1.0},
            {"split": "validation", "method": "Uncertainty gate", "macro_f1": validation["gate"]["macro_f1"], "replacement_fraction": validation["replaced_fraction"]},
            {"split": "test", "method": "TF-IDF base", "macro_f1": base, "replacement_fraction": 0.0},
            {"split": "test", "method": "Qwen direct", "macro_f1": qwen, "replacement_fraction": 1.0},
            {"split": "test", "method": "Uncertainty gate", "macro_f1": gate, "replacement_fraction": test["replaced_fraction"]},
        ]
    )
    source.to_csv(FIGURES / "source_data_fig7.csv", index=False, encoding="utf-8-sig")

    macros = [
        "% Generated by make_gate_assets.py.",
        rf"\newcommand{{\GateBaseMacroF}}{{{100 * base:.2f}}}",
        rf"\newcommand{{\GateQwenMacroF}}{{{100 * qwen:.2f}}}",
        rf"\newcommand{{\GateMacroF}}{{{100 * gate:.2f}}}",
        rf"\newcommand{{\GateDeltaPP}}{{{100 * delta:+.2f}}}",
        rf"\newcommand{{\GateCILowPP}}{{{100 * ci_low:+.2f}}}",
        rf"\newcommand{{\GateCIHighPP}}{{{100 * ci_high:+.2f}}}",
        rf"\newcommand{{\GateMargin}}{{{selection['margin']:.2f}}}",
        rf"\newcommand{{\GateLower}}{{{selection['lower']:.2f}}}",
        rf"\newcommand{{\GateUpper}}{{{selection['upper']:.2f}}}",
        rf"\newcommand{{\GateReplacementPct}}{{{100 * test['replaced_fraction']:.1f}}}",
        rf"\newcommand{{\GateValidationReplacementPct}}{{{100 * validation['replaced_fraction']:.1f}}}",
        rf"\newcommand{{\GateValidationQwenSeconds}}{{{result['qwen_cache']['validation']['sum_recorded_inference_seconds']:.1f}}}",
        rf"\newcommand{{\GateTestQwenSeconds}}{{{result['qwen_cache']['test']['sum_recorded_inference_seconds']:.1f}}}",
    ]
    REPORT.mkdir(parents=True, exist_ok=True)
    (REPORT / "gate_macros.tex").write_text("\n".join(macros) + "\n", encoding="utf-8")

    table = "\n".join(
        [
            r"\begin{tabular}{lrrrr}",
            r"\toprule",
            r"划分 & 方法 & Macro-F1 & 替换比例 & 相对基线 pp\\",
            r"\midrule",
            rf"验证集 & 仅 TF--IDF & {100 * validation['base']['macro_f1']:.2f} & 0.0\% & 0.00\\",
            rf"验证集 & Qwen 直接分类 & {100 * validation['qwen']['macro_f1']:.2f} & 100.0\% & {100 * (validation['qwen']['macro_f1'] - validation['base']['macro_f1']):+.2f}\\",
            rf"验证集 & 不确定性门控 & {100 * validation['gate']['macro_f1']:.2f} & {100 * validation['replaced_fraction']:.1f}\% & {100 * (validation['gate']['macro_f1'] - validation['base']['macro_f1']):+.2f}\\",
            r"\addlinespace",
            rf"测试集 & 仅 TF--IDF & {100 * base:.2f} & 0.0\% & 0.00\\",
            rf"测试集 & Qwen 直接分类 & {100 * qwen:.2f} & 100.0\% & {100 * (qwen - base):+.2f}\\",
            rf"测试集 & 不确定性门控 & {100 * gate:.2f} & {100 * test['replaced_fraction']:.1f}\% & {100 * delta:+.2f}\\",
            r"\bottomrule",
            r"\end{tabular}",
        ]
    )
    (REPORT / "table_gate.tex").write_text(table + "\n", encoding="utf-8")

    fig, axes = plt.subplots(1, 2, figsize=(7.2, 3.0), gridspec_kw={"width_ratios": [1.0, 1.2]})
    colors = {"TF-IDF base": "#606060", "Qwen direct": "#C56A83", "Uncertainty gate": "#42949E"}
    ax = axes[0]
    methods = ["TF-IDF base", "Qwen direct", "Uncertainty gate"]
    values = [base, qwen, gate]
    bars = ax.bar(np.arange(3), np.array(values) * 100, color=[colors[m] for m in methods], width=0.62)
    for bar, value in zip(bars, values):
        ax.text(bar.get_x() + bar.get_width() / 2, value * 100 + 0.35, f"{100 * value:.2f}", ha="center", fontsize=7)
    ax.set_xticks(np.arange(3), ["TF-IDF\nbase", "Qwen\ndirect", "Gate"])
    ax.set_ylabel("Macro-F1 (%)")
    ax.set_ylim(73, 85)
    ax.set_title("Untouched 400-row test", fontsize=8.5, pad=7)
    ax.grid(axis="y", color="#D8D8D8", lw=0.5, zorder=0)
    ax.text(-0.12, 1.03, "a", transform=ax.transAxes, fontsize=10, fontweight="bold")

    ax = axes[1]
    split_names = ["Validation", "Test"]
    base_values = [validation["base"]["macro_f1"] * 100, base * 100]
    gate_values = [validation["gate"]["macro_f1"] * 100, gate * 100]
    x = np.arange(2)
    ax.plot(x, base_values, marker="o", color="#606060", lw=1.5, label="Base")
    ax.plot(x, gate_values, marker="o", color="#42949E", lw=1.5, label="Gate")
    ax.fill_between([1], [base * 100 + 100 * ci_low], [base * 100 + 100 * ci_high], color="#42949E", alpha=0.16, label="Test bootstrap CI")
    ax.set_xticks(x, split_names)
    ax.set_ylabel("Macro-F1 (%)")
    ax.set_ylim(72, 85.5)
    ax.set_title(f"Validation-only margin = {selection['margin']:.2f}; test replacement = {100 * test['replaced_fraction']:.1f}%", fontsize=8.5, pad=7)
    ax.grid(axis="y", color="#D8D8D8", lw=0.5, zorder=0)
    ax.legend(loc="lower right", fontsize=6.6)
    ax.text(-0.10, 1.03, "b", transform=ax.transAxes, fontsize=10, fontweight="bold")
    fig.suptitle("Uncertainty gate: selective Qwen second opinion", fontsize=10, y=1.01)
    fig.tight_layout(pad=1.0, w_pad=1.8)
    base_path = FIGURES / "fig7_uncertainty_gate"
    fig.savefig(base_path.with_suffix(".svg"), bbox_inches="tight")
    fig.savefig(base_path.with_suffix(".pdf"), bbox_inches="tight")
    fig.savefig(base_path.with_suffix(".png"), dpi=300, bbox_inches="tight")
    fig.savefig(base_path.with_suffix(".tiff"), dpi=600, bbox_inches="tight")
    plt.close(fig)
    (FIGURES / "fig7_qa.txt").write_text(
        "Core conclusion: validation-only uncertainty gating improves the fixed 1000/400 TF-IDF system by +1.42 pp on the untouched test point estimate, but the paired bootstrap CI crosses zero.\n"
        "Archetype: quantitative grid. Backend: Python/matplotlib.\n"
        "Source data: source_data_fig7.csv; result source: results/improvement/uncertainty_gate.json.\n"
        "Review risk: Qwen direct labels are an auxiliary second opinion; the validation-selected rule is exploratory and not a replacement for full SPT-RII.\n",
        encoding="utf-8",
    )


if __name__ == "__main__":
    main()
