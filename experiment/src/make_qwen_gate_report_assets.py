"""Generate LaTeX macros and tables for the locked selective-Qwen result."""

from __future__ import annotations

import json
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
RESULTS = ROOT / "experiment" / "results" / "same_budget"
REPORT = ROOT / "experiment" / "report"


def command(name: str, value: str) -> str:
    return rf"\newcommand{{\{name}}}{{{value}}}"


def percent(value: float) -> str:
    return f"{100 * value:.2f}"


def main() -> None:
    result = json.loads((RESULTS / "strict70_qwen_gate_result.json").read_text(encoding="utf-8"))
    analysis = json.loads((RESULTS / "strict70_qwen_gate_analysis.json").read_text(encoding="utf-8"))
    selection = json.loads((RESULTS / "strict70_qwen_gate_selection.json").read_text(encoding="utf-8"))

    assert result["status"] == "completed"
    assert result["protocol_hash"] == analysis["protocol_verification"]["protocol_hash"]
    assert result["selection_hash"] == selection["selection_hash"]
    assert analysis["protocol_verification"]["selection_hash_matches"] is True

    base = analysis["base_metrics"]
    full = analysis["qwen_gate_metrics"]
    routing = analysis["routing"]
    row_boot = analysis["paired_bootstrap"]["row"]
    cluster_boot = analysis["paired_bootstrap"]["live_id_cluster"]
    total = int(result["test_rows"])
    changed = int(routing["changed_rows"])
    qwen_stats = result["qwen_stats"]

    macros = [
        "% Generated by make_qwen_gate_report_assets.py from locked JSON results.",
        command("QwenGateModel", r"Qwen3:8B Q4\_K\_M"),
        command("QwenGateDigestShort", str(result["model_digest"])[:12]),
        command("QwenGateMargin", f"{float(result['selection']['margin']):.2f}"),
        command("QwenGateMinConfidence", f"{float(result['min_confidence']):.0f}"),
        command("QwenGateContextWindow", f"{int(result['context_window'])}"),
        command("QwenGateRetrievalPerClass", f"{int(result['retrieval_per_class'])}"),
        command("QwenGateValidationBase", percent(selection["validation_base_metrics"]["macro_f1"])),
        command("QwenGateValidationFull", percent(selection["selection"]["macro_f1"])),
        command("QwenGateAccuracy", percent(full["accuracy"])),
        command("QwenGatePrecision", percent(full["macro_precision"])),
        command("QwenGateRecall", percent(full["macro_recall"])),
        command("QwenGateFOne", percent(full["macro_f1"])),
        command("QwenGateBaseFOne", percent(base["macro_f1"])),
        command("QwenGateLocalDelta", f"{float(analysis['delta_vs_local_base_pp']):+.2f}"),
        command("QwenGatePaperDelta", f"{float(analysis['delta_vs_paper_spt_rii_pp']):+.2f}"),
        command("QwenGatePaperQwenDelta", f"{float(analysis['delta_vs_paper_qwen_peft_pp']):+.2f}"),
        command("QwenGateTestRows", f"{total:,}"),
        command("QwenGateCandidateRows", f"{int(routing['candidate_rows']):,}"),
        command("QwenGateCandidatePercent", f"{100 * int(routing['candidate_rows']) / total:.2f}"),
        command("QwenGateConsensusRows", f"{int(routing['consensus_rows']):,}"),
        command("QwenGateConsensusPercent", f"{100 * int(routing['consensus_rows']) / total:.2f}"),
        command("QwenGateChangedRows", f"{changed:,}"),
        command("QwenGateChangedPercent", f"{100 * changed / total:.2f}"),
        command("QwenGateCorrectedRows", f"{int(routing['corrected_rows']):,}"),
        command("QwenGateHarmedRows", f"{int(routing['harmed_rows']):,}"),
        command("QwenGateNetRows", f"{int(routing['net_corrected_rows']):+d}"),
        command("QwenGateRowCILow", f"{100 * float(row_boot['delta_ci95'][0]):+.2f}"),
        command("QwenGateRowCIHigh", f"{100 * float(row_boot['delta_ci95'][1]):+.2f}"),
        command("QwenGateRowPNonPositive", f"{float(row_boot['probability_delta_le_zero']):.3f}"),
        command("QwenGateClusterCILow", f"{100 * float(cluster_boot['delta_ci95'][0]):+.2f}"),
        command("QwenGateClusterCIHigh", f"{100 * float(cluster_boot['delta_ci95'][1]):+.2f}"),
        command("QwenGateClusterPNonPositive", f"{float(cluster_boot['probability_delta_le_zero']):.3f}"),
        command("QwenGateRecordedMinutes", f"{float(qwen_stats['historical_recorded_inference_seconds']) / 60:.1f}"),
        command("QwenGateFormalRetrySeconds", f"{float(qwen_stats['this_run_wall_seconds']):.1f}"),
        command("QwenGateNewRetryRecords", f"{int(qwen_stats['new_records'])}"),
        command("QwenGateProtocolHashShort", str(result["protocol_hash"])[:12]),
    ]
    (REPORT / "qwen_gate_macros.tex").write_text("\n".join(macros) + "\n", encoding="utf-8")

    comparison = [
        f"% Local selective-Qwen delta: {float(analysis['delta_vs_local_base_pp']):+.2f} pp; descriptive delta versus Paper Table 5: {float(analysis['delta_vs_paper_spt_rii_pp']):+.2f} pp.",
        r"\begin{tabularx}{\textwidth}{p{1.55cm}p{2.75cm}p{2.35cm}Xrrrr}",
        r"\toprule",
        r"来源 & 方法 & 人工监督 & 大模型角色 & Acc. & Prec. & Rec. & F1 \\",
        r"\midrule",
        r"论文 Table~5 & SPT-RII 70-shot & 70-shot；等大验证集 & 逐条生成解释并输入提示模型 & $80.62\pm0.37$ & $79.96\pm0.32$ & $80.30\pm0.23$ & $79.23\pm0.31$ \\",
        r"论文 Table~6 & Qwen3:8B PEFT & 1,000 样本 & 微调后的独立分类器 & 76.11 & 75.04 & 75.54 & 75.24 \\",
        f"本地严格实验 & DAPT+R-Drop 集成 & 70/类训练与验证 & 推理期不用大模型 & {100 * base['accuracy']:.2f} & {100 * base['macro_precision']:.2f} & {100 * base['macro_recall']:.2f} & {100 * base['macro_f1']:.2f}\\\\",
        rf"\rowcolor{{LightGreen}}本地完整方法 & DAPT+R-Drop + 选择性 Qwen & 同一人工标签集 & 只复核低置信样本 & {100 * full['accuracy']:.2f} & {100 * full['macro_precision']:.2f} & {100 * full['macro_recall']:.2f} & {100 * full['macro_f1']:.2f}\\",
        r"\bottomrule",
        r"\end{tabularx}",
    ]
    (REPORT / "table_qwen_gate_comparison.tex").write_text("\n".join(comparison) + "\n", encoding="utf-8")

    routing_table = [
        r"\begin{tabular}{lrrr}",
        r"\toprule",
        r"路由阶段或结果 & 条数 & 占测试集 & 占改写预测 \\",
        r"\midrule",
        f"完整测试集 & {total:,} & 100.00\\% & --\\\\",
        f"进入 Qwen 的低置信样本 & {int(routing['candidate_rows']):,} & {100 * int(routing['candidate_rows']) / total:.2f}\\% & --\\\\",
        f"Qwen 形成有效共识 & {int(routing['consensus_rows']):,} & {100 * int(routing['consensus_rows']) / total:.2f}\\% & --\\\\",
        f"最终改变小模型预测 & {changed:,} & {100 * changed / total:.2f}\\% & 100.00\\%\\\\",
        f"其中：纠正原错误 & {int(routing['corrected_rows']):,} & {100 * int(routing['corrected_rows']) / total:.2f}\\% & {100 * int(routing['corrected_rows']) / changed:.2f}\\%\\\\",
        f"其中：破坏原正确项 & {int(routing['harmed_rows']):,} & {100 * int(routing['harmed_rows']) / total:.2f}\\% & {100 * int(routing['harmed_rows']) / changed:.2f}\\%\\\\",
        rf"\rowcolor{{LightGreen}}净增加正确预测 & {int(routing['net_corrected_rows']):+d} & {100 * int(routing['net_corrected_rows']) / total:+.2f}\% & --\\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    (REPORT / "table_qwen_gate_routing.tex").write_text("\n".join(routing_table) + "\n", encoding="utf-8")

    print("Wrote qwen_gate_macros.tex and two selective-Qwen tables")


if __name__ == "__main__":
    main()
