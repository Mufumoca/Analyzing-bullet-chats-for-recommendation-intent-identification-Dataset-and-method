ROOT = Path(__file__).resolve().parents[2]
FIGURES_DIR = ROOT / "experiment" / "figures"
REPORT_DIR = ROOT / "experiment" / "report"


def read_json(path: Path) -> dict | list:
    return json.loads(path.read_text(encoding="utf-8"))


def pct(value: float, digits: int = 2) -> str:
    return f"{100 * float(value):.{digits}f}\\%"


def macro(value: float, digits: int = 2) -> str:
    return f"{100 * float(value):.{digits}f}"


def latex_escape(text: str) -> str:
    return (
        str(text)
        .replace("\\", r"\textbackslash{}")
        .replace("&", r"\&")
        .replace("%", r"\%")
        .replace("_", r"\_")
    )


def classical_rows() -> pd.DataFrame:
    rows: list[dict[str, object]] = []
    for size in (400, 1000):
        path = IMPROVEMENT_RESULTS_DIR / f"classical_improvement_{size}.json"
        if not path.exists():
            continue
        for row in read_json(path):
            if row["condition"] in {
                "content",
                "old_concat",
                "structured_concat",
                "old_late_fusion",
                "structured_late_fusion",
            }:
                rows.append({"size": size, **row})
    return pd.DataFrame(rows)


def roberta_rows() -> pd.DataFrame:
    rows: list[dict[str, object]] = []
    for path in sorted(IMPROVEMENT_RESULTS_DIR.glob("roberta_*.json")):
        data = read_json(path)
        if "test_fused_metrics" in data:
            metrics = data["test_fused_metrics"]
            rows.append(
                {
                    "tag": data["tag"],
                    "seed": data["seed"],
                    "train_rows": data["train_rows"],
                    "macro_f1": metrics["macro_f1"],
                    "elapsed_seconds": sum(
                        branch.get("elapsed_seconds", 0.0)
                        for branch in data.get("test_branch_runs", {}).values()
                    ),
                    "peak_memory_mb": max(
                        (branch.get("peak_torch_memory_mb", 0.0)
                         for branch in data.get("test_branch_runs", {}).values()),
                        default=0.0,
                    ),
                    "kind": "late_fusion",
                }
            )
        elif "macro_f1" in data:
            rows.append(
                {
                    "tag": data["tag"],
                    "seed": data["seed"],
                    "train_rows": data["train_rows"],
                    "macro_f1": data["macro_f1"],
                    "elapsed_seconds": data["elapsed_seconds"],
                    "peak_memory_mb": data["peak_torch_memory_mb"],
                    "kind": "concat",
                }
            )
    frame = pd.DataFrame(rows)
    return frame


def existing_roberta_summary() -> pd.DataFrame:
    return pd.read_csv(RESULTS_DIR / "roberta_summary.csv", encoding="utf-8-sig")


def write_macros(classical: pd.DataFrame, roberta: pd.DataFrame) -> dict[str, float]:
    values: dict[str, float] = {}
    lines: list[str] = ["% Generated by make_improvement_assets.py; do not edit manually."]

    def add(name: str, value: float) -> None:
        values[name] = float(value)
        lines.append(rf"\newcommand{{\{name}}}{{{macro(value)}}}")

    for size in (400, 1000):
        subset = classical[classical["size"] == size].set_index("condition")
        scale_name = "Small" if size == 400 else "Large"
        for condition, name in (
            ("content", f"ImproveTFIDF{scale_name}Content"),
            ("old_concat", f"ImproveTFIDF{scale_name}OldConcat"),
            ("structured_concat", f"ImproveTFIDF{scale_name}StructuredConcat"),
            ("old_late_fusion", f"ImproveTFIDF{scale_name}OldLate"),
            ("structured_late_fusion", f"ImproveTFIDF{scale_name}StructuredLate"),
        ):
            if condition in subset.index:
                add(name, subset.loc[condition, "macro_f1"])

    existing = existing_roberta_summary().set_index(["suite", "condition"])
    for suite, condition, name in (
        ("qwen", "content", "ImproveRobertaSmallContent"),
        ("qwen", "qwen", "ImproveRobertaSmallOldQwen"),
    ):
        row = existing.loc[(suite, condition)]
        add(name, row["macro_f1_mean"])

    for tag, name in (
        ("qwen400_structured_concat", "ImproveRobertaSmallStructured"),
        ("qwen400_compact_concat", "ImproveRobertaSmallCompact"),
        ("qwen400_compact_late_fusion", "ImproveRobertaSmallLateFusion"),
        ("content1000", "ImproveRobertaLargeContent"),
        ("qwen1000_structured_concat", "ImproveRobertaLargeStructured"),
        ("qwen1000_compact_concat", "ImproveRobertaLargeCompact"),
        ("qwen1000_compact_late_fusion", "ImproveRobertaLargeLateFusion"),
    ):
        subset = roberta[roberta["tag"] == tag]
        if not subset.empty:
            add(name, subset["macro_f1"].mean())

    # Differences used repeatedly in prose and tables.
    if "ImproveRobertaLargeLateFusion" in values:
        add("ImproveRobertaLargeLateDeltaPP", values["ImproveRobertaLargeLateFusion"] - values["ImproveRobertaLargeContent"])
    if "ImproveRobertaSmallLateFusion" in values:
        add("ImproveRobertaSmallLateDeltaPP", values["ImproveRobertaSmallLateFusion"] - values["ImproveRobertaSmallContent"])
    if "ImproveTFIDFLargeStructuredLate" in values:
        add("ImproveTFIDFLargeLateDeltaPP", values["ImproveTFIDFLargeStructuredLate"] - values["ImproveTFIDFLargeContent"])
    if "ImproveTFIDFSmallOldLate" in values:
        add("ImproveTFIDFSmallOldLateDeltaPP", values["ImproveTFIDFSmallOldLate"] - values["ImproveTFIDFSmallContent"])

    comparisons_path = IMPROVEMENT_RESULTS_DIR / "improvement_comparisons.csv"
    if comparisons_path.exists():
        comparisons = pd.read_csv(comparisons_path, encoding="utf-8-sig")
        for label, prefix in (
            ("old prompt + late fusion vs current", "ImproveTFIDFSmallOldLate"),
            ("1,000 train: structured late fusion vs current", "ImproveTFIDFLargeLate"),
        ):
            subset = comparisons[comparisons["label"] == label]
            if not subset.empty:
                row = subset.iloc[0]
                add(f"{prefix}LowPP", row["ci95_low"])
                add(f"{prefix}HighPP", row["ci95_high"])

    generation_paths = sorted(IMPROVEMENT_RESULTS_DIR.glob("qwen_generation_*.json"))
    for path in generation_paths:
        data = read_json(path)
        scale_name = "Small" if data["size"] == 400 else "Large"
        name = f"ImproveQwen{data['split'].capitalize()}{scale_name}Coverage"
        add(name, data["coverage"])

    REPORT_DIR.mkdir(parents=True, exist_ok=True)
    (REPORT_DIR / "improvement_macros.tex").write_text("\n".join(lines) + "\n", encoding="utf-8")
    return values


def write_tables(classical: pd.DataFrame, roberta: pd.DataFrame) -> None:
    lines = [
        r"\begin{tabular}{llrrr}",
        r"\toprule",
        r"规模 & 条件 & Macro-F1 & 相对当前弹幕 & 选择方式\\",
        r"\midrule",
    ]
    for size in (400, 1000):
        subset = classical[classical["size"] == size].set_index("condition")
        if "content" not in subset.index:
            continue
        base = float(subset.loc["content", "macro_f1"])
        order = ["content", "old_concat", "structured_concat", "old_late_fusion", "structured_late_fusion"]
        labels = {
            "content": "当前弹幕",
            "old_concat": "旧提示词拼接",
            "structured_concat": "结构化提示词拼接",
            "old_late_fusion": "旧提示词晚期融合",
            "structured_late_fusion": "结构化晚期融合",
        }
        for condition in order:
            if condition not in subset.index:
                continue
            row = subset.loc[condition]
            selection = "OOF" if "late" in condition else "固定"
            lines.append(
                f"{size}/400 & {labels[condition]} & {pct(row['macro_f1'])} & "
                f"{(100 * (row['macro_f1'] - base)):+.2f}\\,pp & {selection}" + r"\\"
            )
        if size == 400:
            lines.append(r"\addlinespace")
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    (REPORT_DIR / "table_improvement.tex").write_text("\n".join(lines) + "\n", encoding="utf-8")

    summary_rows = (
        roberta.groupby("tag", as_index=False)
        .agg(mean=("macro_f1", "mean"), sd=("macro_f1", "std"), train_rows=("train_rows", "first"))
    )
    existing = existing_roberta_summary()
    existing_rows = [
        {"tag": "qwen400_content", "mean": existing.query("suite=='qwen' and condition=='content'")["macro_f1_mean"].iloc[0], "sd": existing.query("suite=='qwen' and condition=='content'")["macro_f1_sd"].iloc[0], "train_rows": 400},
        {"tag": "qwen400_old_qwen", "mean": existing.query("suite=='qwen' and condition=='qwen'")["macro_f1_mean"].iloc[0], "sd": existing.query("suite=='qwen' and condition=='qwen'")["macro_f1_sd"].iloc[0], "train_rows": 400},
    ]
    summary = pd.concat([pd.DataFrame(existing_rows), summary_rows], ignore_index=True)
    labels = {
        "qwen400_content": "400 当前弹幕",
        "qwen400_old_qwen": "400 旧 Qwen 拼接",
        "qwen400_structured_concat": "400 结构化拼接",
        "qwen400_compact_concat": "400 紧凑拼接",
        "qwen400_compact_late_fusion": "400 紧凑晚期融合",
        "content1000": "1000 当前弹幕",
        "qwen1000_structured_concat": "1000 结构化拼接",
        "qwen1000_compact_concat": "1000 紧凑拼接",
        "qwen1000_compact_late_fusion": "1000 紧凑晚期融合",
    }
    lines = [r"\begin{tabular}{lrrr}", r"\toprule", r"条件 & 训练条数 & Macro-F1 & SD\\", r"\midrule"]
    row_order = [
        "qwen400_content",
        "qwen400_old_qwen",
        "qwen400_structured_concat",
        "qwen400_compact_concat",
        "qwen400_compact_late_fusion",
        "content1000",
        "qwen1000_structured_concat",
        "qwen1000_compact_concat",
        "qwen1000_compact_late_fusion",
    ]
    summary = summary.set_index("tag")
    for tag in row_order:
        if tag in summary.index:
            row = summary.loc[tag]
            lines.append(
                f"{labels[tag]} & {int(row['train_rows'])} & {pct(row['mean'])} & {pct(row['sd'])}" + r"\\"
            )
    lines.extend([r"\bottomrule", r"\end{tabular}"])
    (REPORT_DIR / "table_roberta_improvement.tex").write_text("\n".join(lines) + "\n", encoding="utf-8")


def make_figure(classical: pd.DataFrame, roberta: pd.DataFrame) -> None:
    existing = existing_roberta_summary()
    fig = plt.figure(figsize=(7.2, 5.4))
    grid = fig.add_gridspec(2, 2, height_ratios=[1.0, 1.0], hspace=0.52, wspace=0.34)
    ax_tf = fig.add_subplot(grid[0, :])
    ax_rb = fig.add_subplot(grid[1, 0])
    ax_cost = fig.add_subplot(grid[1, 1])

    # Panel a: classical classifier values with the original 400-row baseline
    # and the 1,000-row extension shown together.
    rows = []
    for size in (400, 1000):
        subset = classical[classical["size"] == size].set_index("condition")
        for condition in ["content", "old_concat", "structured_concat", "old_late_fusion", "structured_late_fusion"]:
            if condition in subset.index:
                rows.append((f"{size}\n{condition.replace('_', ' ')}", float(subset.loc[condition, "macro_f1"]), size, condition))
    x = np.arange(len(rows))
    colors = []
    for _, _, size, condition in rows:
        if condition == "content":
            colors.append(COLORS["content"])
        elif "late" in condition:
            colors.append(COLORS["positive"])
        elif condition == "structured_concat":
            colors.append(COLORS["qwen"])
        else:
            colors.append(COLORS["official"])
    values = np.array([item[1] for item in rows])
    ax_tf.scatter(x, values, c=colors, s=38, zorder=3)
    for index, value in enumerate(values):
        ax_tf.vlines(index, value - 0.004, value + 0.004, color=colors[index], lw=1.4)
        ax_tf.text(index, value + 0.006, f"{100*value:.1f}", ha="center", va="bottom", fontsize=6.1)
    ax_tf.set_xticks(x, [item[0] for item in rows], rotation=28, ha="right", fontsize=6.2)
    ax_tf.set_ylabel("Macro-F1")
    ax_tf.set_ylim(0.76, 0.86)
    ax_tf.set_title("TF–IDF: OOF late fusion and more training data improve the local system")
    ax_tf.axvline(4.5, color=COLORS["muted"], ls="--", lw=0.8)
    ax_tf.text(2, 0.765, "400-row fixed test", ha="center", va="bottom", fontsize=6.2, color=COLORS["muted"])
    ax_tf.text(7, 0.765, "1,000-row train / same 400-row test", ha="center", va="bottom", fontsize=6.2, color=COLORS["muted"])
    add_panel_label(ax_tf, "a")

    # Panel b: RoBERTa mean ± SD across three seeds.
    existing_index = existing.set_index(["suite", "condition"])
    rb_rows = [
        ("400\ncurrent", float(existing_index.loc[("qwen", "content"), "macro_f1_mean"]), float(existing_index.loc[("qwen", "content"), "macro_f1_sd"]), COLORS["content"]),
        ("400\nold\nQwen", float(existing_index.loc[("qwen", "qwen"), "macro_f1_mean"]), float(existing_index.loc[("qwen", "qwen"), "macro_f1_sd"]), COLORS["official"]),
    ]
    for tag, label, color in [
        ("qwen400_compact_concat", "400\ncompact", COLORS["qwen"]),
        ("qwen400_compact_late_fusion", "400\nlate\nfusion", COLORS["positive"]),
        ("content1000", "1000\ncurrent", COLORS["content"]),
        ("qwen1000_compact_concat", "1000\ncompact", COLORS["qwen"]),
        ("qwen1000_compact_late_fusion", "1000\nlate\nfusion", COLORS["positive"]),
    ]:
        subset = roberta[roberta["tag"] == tag]
        if not subset.empty:
            rb_rows.append((label, subset["macro_f1"].mean(), subset["macro_f1"].std(), color))
    x = np.arange(len(rb_rows))
    vals = np.array([row[1] for row in rb_rows])
    errs = np.array([row[2] for row in rb_rows])
    ax_rb.errorbar(x, vals, yerr=errs, fmt="none", ecolor=COLORS["muted"], capsize=3, lw=1.0, zorder=2)
    ax_rb.scatter(x, vals, c=[row[3] for row in rb_rows], s=34, zorder=3)
    ax_rb.set_xticks(x, [row[0] for row in rb_rows], fontsize=6.0)
    ax_rb.set_ylabel("Macro-F1 (mean ± SD)")
    ax_rb.set_ylim(0.80, 0.89)
    ax_rb.set_title("RoBERTa: compact input plus validation fusion")
    add_panel_label(ax_rb, "b")

    # Panel c: added cost is transparent and modest compared with generation.
    cost_labels = ["Qwen\n400", "Qwen\n1000", "RoBERTa\n400 fusion", "RoBERTa\n1000 fusion"]
    gen400 = float(read_json(IMPROVEMENT_RESULTS_DIR / "qwen_generation_train_improvement400_structured_v2.json")["wall_seconds"])
    gen_test = float(read_json(IMPROVEMENT_RESULTS_DIR / "qwen_generation_test_improvement400_structured_v2.json")["wall_seconds"])
    gen1000 = float(read_json(IMPROVEMENT_RESULTS_DIR / "qwen_generation_train_improvement1000_structured_v2.json")["wall_seconds"])
    fusion400 = float(roberta[roberta["tag"] == "qwen400_compact_late_fusion"]["elapsed_seconds"].mean())
    fusion1000 = float(roberta[roberta["tag"] == "qwen1000_compact_late_fusion"]["elapsed_seconds"].mean())
    cost_values = [gen400 + gen_test, gen1000 + gen_test, fusion400, fusion1000]
    bars = ax_cost.bar(np.arange(4), cost_values, color=[COLORS["qwen"], COLORS["qwen"], COLORS["positive"], COLORS["positive"]], width=0.65)
    ax_cost.set_yscale("log")
    ax_cost.set_xticks(np.arange(4), cost_labels, fontsize=6.2)
    ax_cost.set_ylabel("Recorded wall time (s; log scale)")
    ax_cost.set_title("Additional compute remains measurable")
    for bar, value in zip(bars, cost_values):
        ax_cost.text(bar.get_x() + bar.get_width() / 2, value * 1.12, f"{value:.0f}", ha="center", va="bottom", fontsize=6.2)
    add_panel_label(ax_cost, "c")

    source_rows = [
        {
            "panel": "tfidf",
            "scale": size,
            "condition": condition,
            "macro_f1": value,
            "sd_or_error": "",
        }
        for label, value, size, condition in rows
    ]
    source_rows.extend(
        {
            "panel": "roberta",
            "scale": int(label.splitlines()[0]),
            "condition": label.replace("\n", " "),
            "macro_f1": value,
            "sd_or_error": error,
        }
        for label, value, error, _ in rb_rows
    )
    source_rows.extend(
        {
            "panel": "cost",
            "scale": "",
            "condition": label.replace("\n", " "),
            "macro_f1": "",
            "sd_or_error": value,
        }
        for label, value in zip(cost_labels, cost_values)
    )
    source = pd.DataFrame(source_rows)
    source.to_csv(FIGURES_DIR / "source_data_fig5.csv", index=False, encoding="utf-8-sig")
    fig.suptitle("Strengthened pipeline: compact explanations and leakage-safe fusion", fontsize=10.5, fontweight="bold", y=0.995)
    export_figure(fig, "fig5_improvement_results")


def main() -> None:
    ensure_improvement_dirs()
    classical = classical_rows()
    roberta = roberta_rows()
    if classical.empty or roberta.empty:
        raise RuntimeError("Improvement results are incomplete")
    values = write_macros(classical, roberta)
    write_tables(classical, roberta)
    make_figure(classical, roberta)
    print(json.dumps({"macros": values, "figure": "fig5_improvement_results", "report": str(REPORT_DIR)}, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
